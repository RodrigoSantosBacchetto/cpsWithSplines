std::string completeZeroes(int N) {
    std::stringstream s;
    
    if (N < 10) { s << "00"; }
    else if (N < 100) { s << "0"; }
    
    s << N;
    return s.str();
}

std::string noiseTag(double N) {
    std::stringstream s;

    if (N < 0.0) { s << "NO-NOISE"; }
    else { s << (int)N << "-dB"; }
    
    return s.str();
}

