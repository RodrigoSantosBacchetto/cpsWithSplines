int runExperiment(std::string imgDbBasePath, std::string resultsFolderBasePath, int sampleSize, double noiseLevel) {

    initializeImageClassName();

    std::cout << "Reading image classes...";
    std::vector<std::string> imageClasses = getClassDirectories(imgDbBasePath);
    std::cout << " [DONE]\n";

    // some aux variables, global to the process
    double minRegular = 999999999, minRefined = 999999999;
    double maxRegular = 0, maxRefined = 0;

    std::map< int, std::vector<double> > resultsByScenarioRegular, resultsByScenarioRefined;

    // file and stream definitions for results
    std::stringstream regularResultFileEntry, refinedResultFileEntry, filenamereg, filenameref;
    std::fstream regularResultsFile, refinedResultsFile;

    filenamereg.str("");
    filenamereg << resultsFolderBasePath << EXPERIMENT_NAME << "\\RegularCPSResults_" << completeZeroes(sampleSize)
             << "_" << noiseTag(noiseLevel) << ".txt";

    filenameref.str("");
    filenameref << resultsFolderBasePath << EXPERIMENT_NAME << "\\SplineCPSResults_" << completeZeroes(sampleSize)
             << "_" << noiseTag(noiseLevel) << ".txt";

    // Start experiment
    std::string startingTimestamp = getCurrentTimeAsString();
    std::cout << "Started experiment: " << startingTimestamp << "\n";
    
    for (int i = 0; i < imageClasses.size(); i++) {

        std::cout << "Started processing scenario: " << imageClassName[i] << "\n";

        // defining aux variables for the process
        CPSResult firstClassRootImageRegularCps, secondClassRootImageRegularCps;
        CPSResult firstClassRootImageRefinedCps, secondClassRootImageRefinedCps;

        double averageDisimilarityOnSameClassRegular = 0.0, averageDisimilarityOnSameClassRefined = 0.0;
        double averageDisimilarityOnDiffClassRegular = 0.0, averageDisimilarityOnDiffClassRefined = 0.0;

        std::vector<double> scenarioAveragesRegular, scenarioAveragesRefined;
        
        // Result entries for output file
        regularResultFileEntry.str("");
        regularResultFileEntry << imageClassName[i] << "\t";
        refinedResultFileEntry.str("");
        refinedResultFileEntry << imageClassName[i] << "\t";            

        // Read images for scenario.
        // Images are read in a way that the 1st and 11th image are the "root" images of each class
        std::vector<cv::Mat> classImages = readImagesFromDirectory(imageClasses[i]);
        // Go through the list of images from the specific case and calculate the CPS for each
        std::cout << "Working with CPS Signatures, both regular and refined... \n";
        for (int image = 0; image < classImages.size(); image++) {
            std::vector<cv::Point> fullContour = getKuimContour(classImages[image], ONLY_EXTERNAL_CONTOUR);
            std::vector<cv::Point> workContour;

            // work with noise?
            if (noiseLevel < 0.0) {
                workContour = fullContour;
            } else {
                workContour = getNoisyContour(fullContour, noiseLevel);
            }

            // Compute image's signature
            CPSResult refinedCpsMatrix = generateSplineCpsFromContourAndSampleSize(workContour, sampleSize);
            CPSResult regularCpsMatrix = generateCpsFromContourAndSampleSize(workContour, sampleSize);

            if (image == FIRST_CLASS_ROOT_IMAGE){
                firstClassRootImageRegularCps = regularCpsMatrix;
                firstClassRootImageRefinedCps = refinedCpsMatrix;
                regularResultFileEntry << "\t";
                refinedResultFileEntry << "\t";
            } else {
                double regularDistance = similarityMeasure(firstClassRootImageRegularCps, regularCpsMatrix, 0.5, 0.5);
                double refinedDistance = similarityMeasure(firstClassRootImageRefinedCps, refinedCpsMatrix, 0.5, 0.5);
                
                regularResultFileEntry << regularDistance << "\t";
                refinedResultFileEntry << refinedDistance << "\t";

                if (image <= 5) {
                    averageDisimilarityOnSameClassRegular += regularDistance;
                    averageDisimilarityOnSameClassRefined += refinedDistance;
                } else {
                    averageDisimilarityOnDiffClassRegular += regularDistance;
                    averageDisimilarityOnDiffClassRefined += refinedDistance;
                }

                if (regularDistance < minRegular) { minRegular = regularDistance; }
                if (refinedDistance < minRefined) { minRefined = refinedDistance; }
                if (regularDistance > maxRegular) { maxRegular = regularDistance; }
                if (refinedDistance > maxRefined) { maxRefined = refinedDistance; }
            }
        }

        averageDisimilarityOnSameClassRegular /= 5;
        averageDisimilarityOnSameClassRefined /= 5;
        averageDisimilarityOnDiffClassRegular /= 5;
        averageDisimilarityOnDiffClassRefined /= 5;

        scenarioAveragesRegular.push_back(averageDisimilarityOnSameClassRegular);
        scenarioAveragesRegular.push_back(averageDisimilarityOnDiffClassRegular);
        scenarioAveragesRefined.push_back(averageDisimilarityOnSameClassRefined);
        scenarioAveragesRefined.push_back(averageDisimilarityOnDiffClassRefined);

        regularResultFileEntry << "\t" << averageDisimilarityOnSameClassRegular;
        regularResultFileEntry << "\t" << averageDisimilarityOnDiffClassRegular << "\n";
        refinedResultFileEntry << "\t" << averageDisimilarityOnSameClassRefined;
        refinedResultFileEntry << "\t" << averageDisimilarityOnDiffClassRefined << "\n";
        
        resultsByScenarioRegular.insert({i, scenarioAveragesRegular});
        resultsByScenarioRefined.insert({i, scenarioAveragesRefined});
        
        std::cout << " >> Done. Computed class-specific average distances.\n";

        // Write results to file
        regularResultsFile.open(filenamereg.str().c_str(), std::ios::out | std::ios::app );
        refinedResultsFile.open(filenameref.str().c_str(), std::ios::out | std::ios::app );

        regularResultsFile << regularResultFileEntry.str();
        refinedResultsFile << refinedResultFileEntry.str();

        regularResultsFile.close();
        refinedResultsFile.close();
    }

    std::cout << "\n\nStarted analyzing average results from previous stage...\n";

    double umbralOptimoRegular = 0, errorMinimoRegular = 999999999;
    for (double umbral = minRegular; umbral <= maxRegular; umbral += 0.001){
        double error = 0;
        for (int t = 0; t < resultsByScenarioRegular.size(); t++){
            if ( resultsByScenarioRegular[t][0] > umbral ) { error += 1; }
            if ( resultsByScenarioRegular[t][1] < umbral ) { error += 1; }
        }
        error = error / (2*resultsByScenarioRegular.size());
        if ( error <= errorMinimoRegular ) { errorMinimoRegular = error; umbralOptimoRegular = umbral; }
    }
    std::cout << "\nRegular:\nERROR: " << errorMinimoRegular << "\tUMBRAL: " << umbralOptimoRegular << "\n";
    std::cout << "MIN: " << minRegular << "\tMAX: " << maxRegular << "\n";

    regularResultsFile.open(filenamereg.str().c_str(), std::ios::out | std::ios::app );
    regularResultsFile << "\n\nERROR: " << errorMinimoRegular << "\tUMBRAL: " << umbralOptimoRegular << "\t";
    regularResultsFile << "MIN: " << minRegular << "\tMAX: " << maxRegular << "\n";
    regularResultsFile.close();

    double umbralOptimoRefined = 0, errorMinimoRefined = 999999999;
    for (double umbral = minRefined; umbral <= maxRefined; umbral += 0.001){
        double error = 0;
        for (int t = 0; t < resultsByScenarioRefined.size(); t++){
            if ( resultsByScenarioRefined[t][0] > umbral ) { error += 1; }
            if ( resultsByScenarioRefined[t][1] < umbral ) { error += 1; }
        }
        error = error / (2*resultsByScenarioRefined.size());
        if ( error <= errorMinimoRefined ) { errorMinimoRefined = error; umbralOptimoRefined = umbral; }
    }
    std::cout << "\nRefined:\nERROR: " << errorMinimoRefined << "\tUMBRAL: " << umbralOptimoRefined << "\n";
    std::cout << "MIN: " << minRefined << "\tMAX: " << maxRefined << "\n";

    refinedResultsFile.open(filenameref.str().c_str(), std::ios::out | std::ios::app );
    refinedResultsFile << "\n\nERROR: " << errorMinimoRefined << "\tUMBRAL: " << umbralOptimoRefined << "\t";
    refinedResultsFile << "MIN: " << minRefined << "\tMAX: " << maxRefined << "\n";
    refinedResultsFile.close();

    return 0;
}

std::string getCurrentTimeAsString() {
    time_t t = time(0);   // get time now
    struct tm *now = localtime(&t);

    std::stringstream ss;
    ss << (now->tm_year + 1900)
    << "-" << (now->tm_mon + 1 < 10 ? "0" : "")
    << (now->tm_mon + 1)
    << "-" << (now->tm_mday + 1 < 10 ? "0" : "")
    << now->tm_mday
    << " " << (now->tm_hour + 1 < 10 ? "0" : "")
    << now->tm_hour
    << ":" << (now->tm_min + 1 < 10 ? "0" : "")
    << now->tm_min
    << ":" << (now->tm_sec + 1 < 10 ? "0" : "")
    << now->tm_sec
    << std::endl;

    return ss.str();
}

